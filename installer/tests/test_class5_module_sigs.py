class TestCLI(unittest.TestCase):
    """stdout redirected so CLI print() doesn't pollute test runner."""

    def test_cli_good_path_exits_zero(self):
        with tempfile.TemporaryDirectory() as td:
            tdp = Path(td)
            _write_enforce(tdp, "1")
            _write_keys(tdp, PROC_KEYS_WITH_MACHINE)
            _write_modules(tdp, PROC_MODULES_NONEMPTY)
            with mock.patch(
                "installer.tests.class5_module_sigs.shutil.which",
                return_value="/usr/bin/modinfo",
            ), mock.patch(
                "installer.tests.class5_module_sigs.subprocess.run",
                _mock_modinfo(MODINFO_SIGNED),
            ), contextlib.redirect_stdout(io.StringIO()):
                rc = c5.main([
                    "--enforce-path", str(tdp / "module_sig_enforce"),
                    "--keys-path", str(tdp / "keys"),
                    "--modules-path", str(tdp / "modules"),
                    "--json",
                ])
        self.assertEqual(rc, 0)

    def test_cli_missing_enforce_exits_nonzero(self):
        with tempfile.TemporaryDirectory() as td:
            tdp = Path(td)
            _write_keys(tdp, PROC_KEYS_WITH_MACHINE)
            _write_modules(tdp, PROC_MODULES_EMPTY)
            with contextlib.redirect_stdout(io.StringIO()):
                rc = c5.main([
                    "--enforce-path", str(tdp / "missing"),
                    "--keys-path", str(tdp / "keys"),
                    "--modules-path", str(tdp / "modules"),
                    "--json",
                ])
        self.assertEqual(rc, 1)

    def test_cli_report_only_returns_zero_on_fail(self):
        with tempfile.TemporaryDirectory() as td:
            tdp = Path(td)
            with contextlib.redirect_stdout(io.StringIO()):
                rc = c5.main([
                    "--enforce-path", str(tdp / "missing"),
                    "--keys-path", str(tdp / "missing2"),
                    "--modules-path", str(tdp / "missing3"),
                    "--json", "--report-only",
                ])
        self.assertEqual(rc, 0)


