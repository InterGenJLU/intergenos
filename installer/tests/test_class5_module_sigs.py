class TestParseModinfoFields(unittest.TestCase):
    def test_signed_module_fields(self):
        fields = c5._parse_modinfo_fields(MODINFO_SIGNED)
        for key in ("signer", "sig_id", "sig_hashalgo"):
            self.assertIn(key, fields)

    def test_unsigned_module_missing_fields(self):
        fields = c5._parse_modinfo_fields(MODINFO_UNSIGNED)
        self.assertNotIn("signer", fields)
        self.assertNotIn("sig_id", fields)


