def _write_enforce(td: Path, value: str) -> Path:
    path = td / "module_sig_enforce"
    path.write_text(value)
    return path


def _write_keys(td: Path, content: str) -> Path:
    path = td / "keys"
    path.write_text(content)
    return path


def _write_modules(td: Path, content: str) -> Path:
    path = td / "modules"
    path.write_text(content)
    return path


def _mock_modinfo(stdout: str, returncode: int = 0):
    fake = mock.MagicMock()
    fake.return_value.stdout = stdout
    fake.return_value.stderr = ""
    fake.return_value.returncode = returncode
    return fake


# --- Enforce probe --------------------------------------------------------


class TestProbeModuleSigEnforce(unittest.TestCase):
    def test_file_missing(self):
        with tempfile.TemporaryDirectory() as td:
            r = c5.probe_module_sig_enforce(Path(td) / "missing")
        self.assertFalse(r.passed)
        self.assertIn("not present", r.detail)
        self.assertTrue(r.required)

    def test_enforce_enabled(self):
        with tempfile.TemporaryDirectory() as td:
            path = _write_enforce(Path(td), "1")
            r = c5.probe_module_sig_enforce(path)
        self.assertTrue(r.passed)
        self.assertEqual(r.observed, "1")

    def test_enforce_disabled(self):
        with tempfile.TemporaryDirectory() as td:
            path = _write_enforce(Path(td), "0")
            r = c5.probe_module_sig_enforce(path)
        self.assertFalse(r.passed)
        self.assertEqual(r.observed, "0")
        self.assertIn("OFF", r.detail)

    def test_enforce_garbage(self):
        with tempfile.TemporaryDirectory() as td:
            path = _write_enforce(Path(td), "maybe\n")
            r = c5.probe_module_sig_enforce(path)
        self.assertFalse(r.passed)
        self.assertIn("expected 0 or 1", r.detail)


