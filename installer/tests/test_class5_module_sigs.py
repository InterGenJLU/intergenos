class TestRun(unittest.TestCase):
    def test_all_required_pass(self):
        with tempfile.TemporaryDirectory() as td:
            tdp = Path(td)
            enforce = _write_enforce(tdp, "1")
            keys = _write_keys(tdp, PROC_KEYS_WITH_MACHINE)
            modules = _write_modules(tdp, PROC_MODULES_NONEMPTY)
            with mock.patch(
                "installer.tests.class5_module_sigs.shutil.which",
                return_value="/usr/bin/modinfo",
            ), mock.patch(
                "installer.tests.class5_module_sigs.subprocess.run",
                _mock_modinfo(MODINFO_SIGNED),
            ):
                report = c5.run(enforce, keys, modules)
        self.assertTrue(report.all_required_pass())
        self.assertEqual(report.sampled_module, "nft_masq")
        self.assertEqual(len(report.results), 3)

    def test_enforce_off_fails_overall(self):
        with tempfile.TemporaryDirectory() as td:
            tdp = Path(td)
            enforce = _write_enforce(tdp, "0")
            keys = _write_keys(tdp, PROC_KEYS_WITH_MACHINE)
            modules = _write_modules(tdp, PROC_MODULES_NONEMPTY)
            with mock.patch(
                "installer.tests.class5_module_sigs.shutil.which",
                return_value="/usr/bin/modinfo",
            ), mock.patch(
                "installer.tests.class5_module_sigs.subprocess.run",
                _mock_modinfo(MODINFO_SIGNED),
            ):
                report = c5.run(enforce, keys, modules)
        self.assertFalse(report.all_required_pass())

    def test_no_trust_anchor_keyring_fails_overall(self):
        with tempfile.TemporaryDirectory() as td:
            tdp = Path(td)
            enforce = _write_enforce(tdp, "1")
            keys = _write_keys(tdp, PROC_KEYS_WITHOUT_TRUST_ANCHOR)
            modules = _write_modules(tdp, PROC_MODULES_NONEMPTY)
            with mock.patch(
                "installer.tests.class5_module_sigs.shutil.which",
                return_value="/usr/bin/modinfo",
            ), mock.patch(
                "installer.tests.class5_module_sigs.subprocess.run",
                _mock_modinfo(MODINFO_SIGNED),
            ):
                report = c5.run(enforce, keys, modules)
        self.assertFalse(report.all_required_pass())

    def test_monolithic_kernel_all_required_pass(self):
        """enforce=1 + keyring present + zero loaded modules -> PASS.

        A monolithic kernel is the most-secure posture; Class 5 should
        not mark it a failure.
        """
        with tempfile.TemporaryDirectory() as td:
            tdp = Path(td)
            enforce = _write_enforce(tdp, "1")
            keys = _write_keys(tdp, PROC_KEYS_WITH_MACHINE)
            modules = _write_modules(tdp, PROC_MODULES_EMPTY)
            report = c5.run(enforce, keys, modules)
        self.assertTrue(report.all_required_pass())
        self.assertIsNone(report.sampled_module)

    def test_sample_module_override(self):
        with tempfile.TemporaryDirectory() as td:
            tdp = Path(td)
            enforce = _write_enforce(tdp, "1")
            keys = _write_keys(tdp, PROC_KEYS_WITH_MACHINE)
            modules = _write_modules(tdp, PROC_MODULES_NONEMPTY)
            with mock.patch(
                "installer.tests.class5_module_sigs.shutil.which",
                return_value="/usr/bin/modinfo",
            ), mock.patch(
                "installer.tests.class5_module_sigs.subprocess.run",
                _mock_modinfo(MODINFO_SIGNED),
            ):
                report = c5.run(enforce, keys, modules,
                                sample_module="vfat")
        self.assertEqual(report.sampled_module, "vfat")
        self.assertTrue(report.all_required_pass())

    def test_json_report_shape(self):
        import json as _json
        with tempfile.TemporaryDirectory() as td:
            tdp = Path(td)
            enforce = _write_enforce(tdp, "1")
            keys = _write_keys(tdp, PROC_KEYS_WITH_MACHINE)
            modules = _write_modules(tdp, PROC_MODULES_NONEMPTY)
            with mock.patch(
                "installer.tests.class5_module_sigs.shutil.which",
                return_value="/usr/bin/modinfo",
            ), mock.patch(
                "installer.tests.class5_module_sigs.subprocess.run",
                _mock_modinfo(MODINFO_SIGNED),
            ):
                report = c5.run(enforce, keys, modules)
                d = report.to_dict()
        reloaded = _json.loads(_json.dumps(d))
        self.assertTrue(reloaded["all_required_pass"])
        self.assertEqual(reloaded["sampled_module"], "nft_masq")
        self.assertEqual(len(reloaded["results"]), 3)


